package conditional1;

public class IfElse {

	public static void main(String[] args) {
		int age=25;
		if(age==18)
		    {
		       System.out.println("Mechanical Engineering");
		    }
		    else if(age>18)
		    {
		       System.out.println("Automobile Engineering");
		    }
		    else
		    {
		    	System.out.println("Electrical Engineering");
		   }
	}

}
